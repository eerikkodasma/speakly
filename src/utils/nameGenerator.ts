export function generateNames(maxNum: number) {
  let users = [];
  let totalUsers = 0;
  outerLoop: for (let nameIndex = 0; nameIndex <= names.length; nameIndex++) {
    for (let userIndex = 1; userIndex <= 10; userIndex++) {
      if (totalUsers === maxNum) break outerLoop;
      users.push({
        id: totalUsers + 1,
        userName: `${names[nameIndex]}-${userIndex}`,
        score: Math.floor(Math.random() * 501),
        rank: 0,
        previousRank: 0,
        pointsGained: 0,
      });
      totalUsers += 1;
    }
  }
  return users;
}
